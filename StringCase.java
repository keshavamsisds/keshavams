/* Show given string in upper and lower case */
package com.myjava.string;
public class StringCase
	{
		public static void main (String a[])
			{
				String str = "This is a class room";
				
				System.out.println("Upper of Input String is" + str.toUpperCase());
				System.out.println("Lower of Input String is" +str.toLowerCase()); 
			}
    }
	